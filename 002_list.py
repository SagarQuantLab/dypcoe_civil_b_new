# LIST
# [], ordered, duplicates allowed, index, mutable

my_list = [1, 3, 5, 8, 5]
print(my_list)
print(my_list)

# access first element
print(my_list[0])

# access second element
print(my_list[1])

# last element access
print(my_list[-1])

# list reverse
my_list.reverse()
print(my_list)

# list append
my_list.append("Apple")
print(my_list)

# change value of first element to 1000
my_list[0] = 1000
print(my_list)